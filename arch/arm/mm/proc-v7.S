/*
 *	cpu_v7_switch_mm(pgd_phys, tsk)
 *
 *	Set the translation table base pointer to be pgd_phys
 *
 *	- pgd_phys - physical address of new TTB
 *
 *	It is assumed that:
 *	- we are not using split page tables
 */
ENTRY(cpu_v7_switch_mm)
#ifdef CONFIG_MMU
	mov	r2, #0
	ldr	r1, [r1, #MM_CONTEXT_ID]	@ get mm->context.id
	ALT_SMP(orr	r0, r0, #TTB_FLAGS_SMP)
	ALT_UP(orr	r0, r0, #TTB_FLAGS_UP)
#ifdef CONFIG_ARM_ERRATA_430973
	mcr	p15, 0, r2, c7, c5, 6		@ flush BTAC/BTB
#endif
#if defined(MY_DEF_HERE) || defined(MY_DEF_HERE)
	mrc	p15, 0, r2, c2, c0, 1		@ load TTB 1
	mcr	p15, 0, r2, c2, c0, 0		@ into TTB 0
#else
#ifdef CONFIG_ARM_ERRATA_754322
	dsb
#endif
	mcr	p15, 0, r2, c13, c0, 1		@ set reserved context ID
	isb
1:	mcr	p15, 0, r0, c2, c0, 0		@ set TTB 0
#endif
	isb
#ifdef CONFIG_ARM_ERRATA_754322
	dsb
#endif
	mcr	p15, 0, r1, c13, c0, 1		@ set context ID
	isb
#if defined(MY_DEF_HERE) || defined(MY_DEF_HERE)
	mcr	p15, 0, r0, c2, c0, 0		@ set TTB 0
	isb
#endif
#endif
	mov	pc, lr
ENDPROC(cpu_v7_switch_mm)

/*
 *	cpu_v7_set_pte_ext(ptep, pte)
 *
 *	Set a level 2 translation table entry.
 *
 *	- ptep  - pointer to level 2 translation table entry
 *		  (hardware version is stored at +2048 bytes)
 *	- pte   - PTE value to store
 *	- ext	- value for extended PTE bits
 */
ENTRY(cpu_v7_set_pte_ext)
#ifdef CONFIG_MMU
	str	r1, [r0]			@ linux version

#if defined(MY_DEF_HERE)
	/* Calc HW PTE Entry Offset */
#ifdef CONFIG_MV_LARGE_PAGE_SUPPORT
#ifdef CONFIG_MV_8KB_SW_PAGE_SIZE_SUPPORT
	mov	r3, #0x3FC
	and	r3, r3, r0
	mov	r3, r3, lsl#1
#elif defined(CONFIG_MV_16KB_SW_PAGE_SIZE_SUPPORT)
	mov	r3, #0x1FC
	and	r3, r3, r0
	mov	r3, r3, lsl#2
#elif defined(CONFIG_MV_32KB_SW_PAGE_SIZE_SUPPORT)
	mov	r3, #0xFC
	and	r3, r3, r0
	mov	r3, r3, lsl#3
#elif defined(CONFIG_MV_64KB_SW_PAGE_SIZE_SUPPORT)
	mov	r3, #0x7C
	and	r3, r3, r0
	mov	r3, r3, lsl #4
#elif defined(CONFIG_MV_64KB_MMU_PAGE_SIZE_SUPPORT)
	mov	r3, #0x7C
	and	r3, r3, r0
	mov	r3, r3, lsl#4
#endif /* CONFIG_MV_8KB_SW_PAGE_SIZE_SUPPORT */
	bic	r0, r0, #0x3FC
	bic     r0, r0, #0x400
	orr	r0, r0, r3
#endif /* CONFIG_MV_LARGE_PAGE_SUPPORT */
#endif // MY_DEF_HERE	
	bic	r3, r1, #0x000003f0
#if defined(MY_DEF_HERE) && defined(CONFIG_MV_64KB_MMU_PAGE_SIZE_SUPPORT)
	bic	r3, r3, #0x00000F000
#endif
	bic	r3, r3, #PTE_TYPE_MASK
	orr	r3, r3, r2
#if !defined(MY_ABC_HERE) || !defined(CONFIG_COMCERTO_64K_PAGES)
#if defined(MY_DEF_HERE) && defined(CONFIG_MV_64KB_MMU_PAGE_SIZE_SUPPORT)
	orr	r3, r3, #PTE_EXT_AP0 | 1
#else
	orr	r3, r3, #PTE_EXT_AP0 | 2
#endif
#else
	orr	r3, r3, #PTE_EXT_AP0 | 1
#endif

	tst	r1, #1 << 4
	orrne	r3, r3, #PTE_EXT_TEX(1)

	eor	r1, r1, #L_PTE_DIRTY
	tst	r1, #L_PTE_RDONLY | L_PTE_DIRTY
	orrne	r3, r3, #PTE_EXT_APX

	tst	r1, #L_PTE_USER
	orrne	r3, r3, #PTE_EXT_AP1
#ifdef CONFIG_CPU_USE_DOMAINS
	@ allow kernel read/write access to read-only user pages
	tstne	r3, #PTE_EXT_APX
	bicne	r3, r3, #PTE_EXT_APX | PTE_EXT_AP0
#endif

	tst	r1, #L_PTE_XN
	orrne	r3, r3, #PTE_EXT_XN

	tst	r1, #L_PTE_YOUNG
	tstne	r1, #L_PTE_PRESENT
	moveq	r3, #0

#if !defined(MY_ABC_HERE) || !defined(CONFIG_COMCERTO_64K_PAGES)
 ARM(	str	r3, [r0, #2048]! )
 THUMB(	add	r0, r0, #2048 )
 THUMB(	str	r3, [r0] )
#if defined(MY_DEF_HERE) && defined(CONFIG_MV_LARGE_PAGE_SUPPORT)
#ifdef CONFIG_MV_64KB_MMU_PAGE_SIZE_SUPPORT
       @ Need to duplicate the entry 16 times because of overlapping in PTE index bits.
       str     r3, [r0, #4]
       str     r3, [r0, #8]
       str     r3, [r0, #12]
       str     r3, [r0, #16]
       str     r3, [r0, #20]
       str     r3, [r0, #24]
       str     r3, [r0, #28]
	mcr	p15, 0, r0, c7, c10, 1		@ flush_pte
       add     r0, r0, #32
       str     r3, [r0]
       str     r3, [r0, #4]
       str     r3, [r0, #8]
       str     r3, [r0, #12]
       str     r3, [r0, #16]
       str     r3, [r0, #20]
       str     r3, [r0, #24]
       str     r3, [r0, #28]
	mcr	p15, 0, r0, c7, c10, 1		@ flush_pte
#else
#if PAGE_SHIFT > 12 		// >= 8KB
	add	r3, r3, #0x1000
	str     r3, [r0, #4]
#endif /* PAGE_SHIFT > 12 */
#if PAGE_SHIFT > 13		// >= 16KB
	add	r3, r3, #0x1000
	str     r3, [r0, #8]
	add	r3, r3, #0x1000
	str     r3, [r0, #12]
#endif /* PAGE_SHIFT > 13 */
#if PAGE_SHIFT > 14		// >= 32KB
	add	r3, r3, #0x1000
	str     r3, [r0, #16]
	add	r3, r3, #0x1000
	str     r3, [r0, #20]
	add	r3, r3, #0x1000
	str     r3, [r0, #24]
	add	r3, r3, #0x1000
	str     r3, [r0, #28]
#endif /* PAGE_SHIFT > 14 */
	mcr	p15, 0, r0, c7, c10, 1		@ flush_pte
#if PAGE_SHIFT > 15		// >= 64KB
	add	r0, r0, #32
	add	r3, r3, #0x1000
	str     r3, [r0]
	add	r3, r3, #0x1000
	str     r3, [r0, #4]
	add	r3, r3, #0x1000
	str     r3, [r0, #8]
	add	r3, r3, #0x1000
	str     r3, [r0, #12]
	add	r3, r3, #0x1000
	str     r3, [r0, #16]
	add	r3, r3, #0x1000
	str     r3, [r0, #20]
	add	r3, r3, #0x1000
	str     r3, [r0, #24]
	add	r3, r3, #0x1000
	str     r3, [r0, #28]
	mcr	p15, 0, r0, c7, c10, 1		@ flush_pte
#endif /* PAGE_SHIFT > 15 */
#endif /* CONFIG_MV_64KB_MMU_PAGE_SIZE_SUPPORT */
#else /* CONFIG_MV_LARGE_PAGE_SUPPORT */
	mcr	p15, 0, r0, c7, c10, 1		@ flush_pte
#endif /* CONFIG_MV_LARGE_PAGE_SUPPORT */

 #else
	ldr r1, =32768		@ PTE_HWTABLE_OFF
	add r1, r1, r0
	add r2, r1, #64		@ 16*4 bytes
 1:
	sub r2, r2, #4
 	str	r3, [r2]!
	cmp r1, r2
	blo 1b
	mcr	p15, 0, r1, c7, c10, 1		@ flush_pte
	add r1, r1, #32				@ 1 cache line, since HW PTE takes 64 bytes
	mcr	p15, 0, r1, c7, c10, 1		@ flush_pte
 #endif

#endif
	mov	pc, lr
ENDPROC(cpu_v7_set_pte_ext)
#endif // end of MY_DEF_HERE

